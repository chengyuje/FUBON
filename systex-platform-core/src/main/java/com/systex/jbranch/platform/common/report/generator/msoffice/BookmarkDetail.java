package com.systex.jbranch.platform.common.report.generator.msoffice;

public class BookmarkDetail{
	
	private Object data;
	private boolean isText;
	private boolean isPageCode;
	
	public BookmarkDetail(Object data, boolean isText){
		this.data = data;
		this.isText = isText;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}

	public boolean isText() {
		return isText;
	}

	public void setText(boolean isText) {
		this.isText = isText;
	}

	public boolean isPageCode() {
		return isPageCode;
	}

	public void setPageCode(boolean isPageCode) {
		this.isPageCode = isPageCode;
	}
}
