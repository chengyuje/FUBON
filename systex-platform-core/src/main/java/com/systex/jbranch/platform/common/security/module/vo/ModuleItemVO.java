/** 
 * 模組交易類別
 * @author Hong-jie
 * @version 1.0 2008/4/14
 */
public class ModuleItemVO {

	/**
 * 此模組交易的ID
 */
private String itemid;
 

	/**
 * 此模組交易的名稱
 */
private String itemname;
 
/**
 * 取得此模組交易的名稱
 * Getter of the property <tt>itemname</tt>
 * @return Returns the itemname. 
*/
public String getItemname(){
return itemname;
	 }

/**
 * 設定此模組交易的名稱
 * Setter of the property <tt>itemname</tt>
 * @param itemname The itemname to set. 
*/
public void setItemname(String itemname){
this.itemname = itemname;
	 }

	/**
 * 此模組交易的描述
 */
private String description;
 
/**
 * 取得此模組交易的描述
 * Getter of the property <tt>description</tt>
 * @return Returns the description. 
*/
public String getDescription(){
return description;
	 }

/**
 * 設定此模組交易的描述
 * Setter of the property <tt>description</tt>
 * @param description The description to set. 
*/
public void setDescription(String description){
this.description = description;
	 }

	/**
 * 此模組交易是否上架flag
 */
private String apply;
 
/**
 * 取得此模組交易是否上架flag
 * Getter of the property <tt>apply</tt>
 * @return Returns the apply. 
*/
public String getApply(){
return apply;
	 }

/**
 * 設定此模組交易是否上架
 * Setter of the property <tt>apply</tt>
 * @param apply The apply to set. 
*/
public void setApply(String apply){
this.apply = apply;
	 }

	/**
 * 取得此模組交易的ID
 * Getter of the property <tt>itemid</tt>
 * @return Returns the itemid. 
*/
public String getItemid(){
	return itemid;
	 }

/**
 * 設定此模組交易ID
 * Setter of the property <tt>itemid</tt>
 * @param itemid The itemid to set. 
*/
public void setItemid(String itemid){
this.itemid = itemid;
	 }

}

