package com.systex.jbranch.platform.common.security.privilege.vo;

/** 
 * 權限VO
 * @author Hong-jie
 * @version 1.0 2008/4/14
 */
public class PrivilegeVO {

	/**
 * 權限ID
 */
private String privilegeid;
 
/**
 * 取得權限ID
 * Getter of the property <tt>privilegeid</tt>
 * @return Returns the privilegeid. 
*/
public String getPrivilegeid(){
return privilegeid;
	 }

/**
 * 設定權限ID
 * Setter of the property <tt>privilegeid</tt>
 * @param privilegeid The privilegeid to set. 
*/
public void setPrivilegeid(String privilegeid){
this.privilegeid = privilegeid;
	 }

	/**
 * 權限名稱
 */
private String name;
 
/**
 * 取得權限名稱
 * Getter of the property <tt>name</tt>
 * @return Returns the name. 
*/
public String getName(){
return name;
	 }

/**
 * 設定權限名稱
 * Setter of the property <tt>name</tt>
 * @param name The name to set. 
*/
public void setName(String name){
this.name = name;
	 }

	/**
 * 權限的描述
 */
private String description;
 
/**
 * 取得權限描述
 * Getter of the property <tt>description</tt>
 * @return Returns the description. 
*/
public String getDescription(){
return description;
	 }

/**
 * 設定權限描述
 * Setter of the property <tt>description</tt>
 * @param description The description to set. 
*/
public void setDescription(String description){
this.description = description;
	 }

	/**
 * 可執行 或不可執行
 * 
 */
private String operation;
 
/**
 * 取得是否可以執行
 * Getter of the property <tt>operation</tt>
 * @return Returns the operation. 
*/
public String getOperation(){
return operation;
	 }

/**
 * 設定是否可執行
 * Setter of the property <tt>operation</tt>
 * @param operation The operation to set. 
*/
public void setOperation(String operation){
this.operation = operation;
	 }

}

