package com.systex.jbranch.platform.common.excel;

public class RowAndColumn {
	private int row;
	private int column;
	
	RowAndColumn(){
		
	}
	
	RowAndColumn(int row, int column) {
		this.row = row;
		this.column = column;
	}
	int getRow() {
		return row;
	}
	void setRow(int row) {
		this.row = row;
	}
	int getColumn() {
		return column;
	}
	void setColumn(int column) {
		this.column = column;
	}
}
