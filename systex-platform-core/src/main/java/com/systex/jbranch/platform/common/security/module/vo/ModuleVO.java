package com.systex.jbranch.platform.common.security.module.vo;

/** 
 * 功能模組類別
 * @author Hong-jie
 * @version 1.0 2008/4/14
 */
public class ModuleVO {

	/**
 * 此功能模組的名稱
 */
private String modulename;
 
/**
 * 設定此功能模組的名稱
 * Getter of the property <tt>modulename</tt>
 * @return Returns the modulename. 
*/
public String getModulename(){
return modulename;
	 }

/**
 * 設定此功能模組的名稱
 * Setter of the property <tt>modulename</tt>
 * @param modulename The modulename to set. 
*/
public void setModulename(String modulename){
this.modulename = modulename;
	 }

	/**
 * 此功能模組ID
 */
private String moduleid;
 
/**
 * 取得此功能模組的ID
 * Getter of the property <tt>moduleid</tt>
 * @return Returns the moduleid. 
*/
public String getModuleid(){
return moduleid;
	 }

/**
 * 設定此功能模組的ID
 * Setter of the property <tt>moduleid</tt>
 * @param moduleid The moduleid to set. 
*/
public void setModuleid(String moduleid){
this.moduleid = moduleid;
	 }

	/**
 * 此功能模組的描述
 */
private String description;
 
/**
 * 取得此功能模組的描述
 * Getter of the property <tt>description</tt>
 * @return Returns the description. 
*/
public String getDescription(){
return description;
	 }

/**
 * 設定此功能模組的描述
 * Setter of the property <tt>description</tt>
 * @param description The description to set. 
*/
public void setDescription(String description){
this.description = description;
	 }

	/**
 * 此功能模組是否上架flag
 */
private String apply;
 
/**
 * 取得此功能模組是否上架flag
 * Getter of the property <tt>apply</tt>
 * @return Returns the apply. 
*/
public String getApply(){
return apply;
	 }

/**
 * 設定此功能模組是否上架
 * Setter of the property <tt>apply</tt>
 * @param apply The apply to set. 
*/
public void setApply(String apply){
this.apply = apply;
	 }

}

