package com.systex.jbranch.workflow;

import java.util.List;

/**
 * @author Alex Lin
 * @version 2010/02/09 9:36:23 AM
 */
public class Repository {
// ------------------------------ FIELDS ------------------------------

    private String dir;
    private List<String> fileNames;
    private List<String> regexPatterns;

// --------------------- GETTER / SETTER METHODS ---------------------

    public String getDir() {
        return dir;
    }

    public void setDir(String dir) {
        this.dir = dir;
    }

    public List<String> getFileNames() {
        return fileNames;
    }

    public void setFileNames(List<String> fileNames) {
        this.fileNames = fileNames;
    }

    public List<String> getRegexPatterns() {
        return regexPatterns;
    }

    public void setRegexPatterns(List<String> regexPatterns) {
        this.regexPatterns = regexPatterns;
    }

// ------------------------ CANONICAL METHODS ------------------------

    @Override
    public String toString() {
        return "com.systex.jbranch.workflow.Repository{" +
                "dir='" + dir + '\'' +
                ", fileNames=" + fileNames +
                ", regexPatterns=" + regexPatterns +
                '}';
    }
}
