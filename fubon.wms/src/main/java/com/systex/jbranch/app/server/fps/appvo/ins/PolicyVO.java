package com.systex.jbranch.app.server.fps.appvo.ins;


public class PolicyVO {
	private String policyID;
	private String policyNo;
	private String payType;
	private String signYY;
	private String signMM;
	private String signDD;
	private String policyStatus;
	
	public String getPolicyID() {
		return policyID;
	}
	public void setPolicyID(String policyID) {
		this.policyID = policyID;
	}
	public String getPolicyNo() {
		return policyNo;
	}
	public void setPolicyNo(String policyNo) {
		this.policyNo = policyNo;
	}
	public String getPayType() {
		return payType;
	}
	public void setPayType(String payType) {
		this.payType = payType;
	}
	public String getSignYY() {
		return signYY;
	}
	public void setSignYY(String signYY) {
		this.signYY = signYY;
	}
	public String getSignMM() {
		return signMM;
	}
	public void setSignMM(String signMM) {
		this.signMM = signMM;
	}
	public String getSignDD() {
		return signDD;
	}
	public void setSignDD(String signDD) {
		this.signDD = signDD;
	}
	public String getPolicyStatus() {
		return policyStatus;
	}
	public void setPolicyStatus(String policyStatus) {
		this.policyStatus = policyStatus;
	}
}